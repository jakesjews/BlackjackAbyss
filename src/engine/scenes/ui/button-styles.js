function freezeStyleSet(styleSet) {
  return Object.freeze({
    idle: Object.freeze({ ...styleSet.idle }),
    hover: Object.freeze({ ...styleSet.hover }),
    pressed: Object.freeze({ ...styleSet.pressed }),
    disabled: Object.freeze({ ...styleSet.disabled }),
    ...(styleSet.locked ? { locked: Object.freeze({ ...styleSet.locked }) } : {}),
    ...(styleSet.sold ? { sold: Object.freeze({ ...styleSet.sold }) } : {}),
    ...(styleSet.warn ? { warn: Object.freeze({ ...styleSet.warn }) } : {}),
  });
}

